package Domain;

public class Zona {

    private String nombre;
    private double precioPlus;

    //Getters
    public String getNombre() {
        return nombre;
    }
    public double getPrecioPlus() {
        return precioPlus;
    }

    //Setters
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    public void setPrecioPlus(double precioPlus) {
        this.precioPlus = precioPlus;
    }
}
